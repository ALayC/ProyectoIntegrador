using Microsoft.EntityFrameworkCore;
using ProyectoIntegrador.Data.Entities;

namespace ProyectoIntegrador.Data.Context;

public class AppDbContext : DbContext
{
    public AppDbContext(DbContextOptions<AppDbContext> options) : base(options)
    {
    }

    // Seguridad y acceso
    public DbSet<Usuario> Usuarios => Set<Usuario>();
    public DbSet<Rol> Roles => Set<Rol>();
    public DbSet<Permiso> Permisos => Set<Permiso>();
    public DbSet<RolPermiso> RolPermisos => Set<RolPermiso>();
    public DbSet<TokenRevocado> TokensRevocados => Set<TokenRevocado>();

    // Clientes y estructura contable
    public DbSet<Cliente> Clientes => Set<Cliente>();
    public DbSet<PlanDeCuentas> PlanesDeCuentas => Set<PlanDeCuentas>();
    public DbSet<CuentaContable> CuentasContables => Set<CuentaContable>();
    public DbSet<EjercicioContable> EjerciciosContables => Set<EjercicioContable>();

    // Núcleo transaccional
    public DbSet<AsientoContable> AsientosContables => Set<AsientoContable>();
    public DbSet<LineaAsiento> LineasAsiento => Set<LineaAsiento>();
    public DbSet<Comprobante> Comprobantes => Set<Comprobante>();
    public DbSet<Importacion> Importaciones => Set<Importacion>();
    public DbSet<SaldoCuenta> SaldosCuenta => Set<SaldoCuenta>();

    // Soporte transversal
    public DbSet<CentroDeCosto> CentrosDeCosto => Set<CentroDeCosto>();
    public DbSet<TipoDeCambio> TiposDeCambio => Set<TipoDeCambio>();
    public DbSet<Auditoria> Auditorias => Set<Auditoria>();

    protected override void OnModelCreating(ModelBuilder modelBuilder)
    {
        base.OnModelCreating(modelBuilder);

        // ??????????????????????????????????????????????
        // Rol
        // ??????????????????????????????????????????????
        modelBuilder.Entity<Rol>(entity =>
      {
          entity.HasKey(e => e.Id);

          entity.HasMany(e => e.Usuarios)
          .WithOne(e => e.Rol)
 .HasForeignKey(e => e.RolId)
 .OnDelete(DeleteBehavior.Restrict);
      });

        // ??????????????????????????????????????????????
        // Permiso
        // ??????????????????????????????????????????????
        modelBuilder.Entity<Permiso>(entity =>
        {
            entity.HasKey(e => e.Id);
        });

        // ??????????????????????????????????????????????
        // RolPermiso (clave compuesta)
        // ??????????????????????????????????????????????
        modelBuilder.Entity<RolPermiso>(entity =>
        {
            entity.HasKey(e => new { e.RolId, e.PermisoId });

            entity.HasOne(e => e.Rol)
            .WithMany(e => e.RolPermisos)
       .HasForeignKey(e => e.RolId)
      .OnDelete(DeleteBehavior.Restrict);

            entity.HasOne(e => e.Permiso)
           .WithMany(e => e.RolPermisos)
              .HasForeignKey(e => e.PermisoId)
                .OnDelete(DeleteBehavior.Restrict);
        });

        // ??????????????????????????????????????????????
        // Usuario
        // ??????????????????????????????????????????????
        modelBuilder.Entity<Usuario>(entity =>
        {
            entity.HasKey(e => e.Id);

            // Índice único de email
            entity.HasIndex(e => e.Email).IsUnique();

            // Autorreferencia: Contador ? Auxiliares (DeleteBehavior.Restrict)
            entity.HasOne(e => e.Contador)
           .WithMany(e => e.Auxiliares)
        .HasForeignKey(e => e.ContadorId)
          .OnDelete(DeleteBehavior.Restrict);

            entity.HasMany(e => e.Clientes)
             .WithOne(e => e.Contador)
        .HasForeignKey(e => e.ContadorId)
       .OnDelete(DeleteBehavior.Restrict);

            entity.HasMany(e => e.Asientos)
 .WithOne(e => e.Usuario)
           .HasForeignKey(e => e.UsuarioId)
    .OnDelete(DeleteBehavior.Restrict);

            entity.HasMany(e => e.Importaciones)
         .WithOne(e => e.Usuario)
        .HasForeignKey(e => e.UsuarioId)
          .OnDelete(DeleteBehavior.Restrict);

            entity.HasMany(e => e.TokensRevocados)
  .WithOne(e => e.Usuario)
         .HasForeignKey(e => e.UsuarioId)
         .OnDelete(DeleteBehavior.Restrict);

            entity.HasMany(e => e.Auditorias)
              .WithOne(e => e.Usuario)
           .HasForeignKey(e => e.UsuarioId)
             .OnDelete(DeleteBehavior.Restrict);
        });

        // ??????????????????????????????????????????????
        // TokenRevocado
        // ??????????????????????????????????????????????
        modelBuilder.Entity<TokenRevocado>(entity =>
         {
             entity.HasKey(e => e.Id);
         });

        // ??????????????????????????????????????????????
        // Cliente
        // ??????????????????????????????????????????????
        modelBuilder.Entity<Cliente>(entity =>
               {
                   entity.HasKey(e => e.Id);

                   // Índice único de RUT
                   entity.HasIndex(e => e.Rut).IsUnique();

                   entity.HasMany(e => e.Asientos)
    .WithOne(e => e.Cliente)
           .HasForeignKey(e => e.ClienteId)
                    .OnDelete(DeleteBehavior.Restrict);

                   entity.HasMany(e => e.Ejercicios)
                 .WithOne(e => e.Cliente)
            .HasForeignKey(e => e.ClienteId)
                  .OnDelete(DeleteBehavior.Restrict);

                   entity.HasMany(e => e.Comprobantes)
               .WithOne(e => e.Cliente)
         .HasForeignKey(e => e.ClienteId)
                 .OnDelete(DeleteBehavior.Restrict);

                   entity.HasMany(e => e.CentrosDeCosto)
             .WithOne(e => e.Cliente)
            .HasForeignKey(e => e.ClienteId)
                 .OnDelete(DeleteBehavior.Restrict);

                   entity.HasMany(e => e.Importaciones)
           .WithOne(e => e.Cliente)
      .HasForeignKey(e => e.ClienteId)
             .OnDelete(DeleteBehavior.Restrict);

                   entity.HasMany(e => e.SaldosCuenta)
           .WithOne(e => e.Cliente)
        .HasForeignKey(e => e.ClienteId)
          .OnDelete(DeleteBehavior.Restrict);
               });

        // ??????????????????????????????????????????????
        // PlanDeCuentas (relación 1:1 con Cliente)
        // ??????????????????????????????????????????????
        modelBuilder.Entity<PlanDeCuentas>(entity =>
        {
            entity.HasKey(e => e.Id);

            // Relación 1:1 con Cliente
            entity.HasIndex(e => e.ClienteId).IsUnique();

            entity.HasOne(e => e.Cliente)
       .WithOne(e => e.PlanDeCuentas)
      .HasForeignKey<PlanDeCuentas>(e => e.ClienteId)
       .OnDelete(DeleteBehavior.Restrict);

            entity.HasMany(e => e.CuentasContables)
              .WithOne(e => e.PlanDeCuentas)
                   .HasForeignKey(e => e.PlanCuentasId)
              .OnDelete(DeleteBehavior.Restrict);
        });

        // ??????????????????????????????????????????????
        // CuentaContable
        // ??????????????????????????????????????????????
        modelBuilder.Entity<CuentaContable>(entity =>
                {
                    entity.HasKey(e => e.Id);

                    // Índice único de código dentro del plan de cuentas
                    entity.HasIndex(e => new { e.PlanCuentasId, e.Codigo }).IsUnique();

                    // Autorreferencia: CuentaPadre ? CuentasHijas
                    entity.HasOne(e => e.CuentaPadre)
                    .WithMany(e => e.CuentasHijas)
             .HasForeignKey(e => e.CuentaPadreId)
              .OnDelete(DeleteBehavior.Restrict);

                    entity.HasMany(e => e.LineasAsiento)
       .WithOne(e => e.CuentaContable)
         .HasForeignKey(e => e.CuentaContableId)
                 .OnDelete(DeleteBehavior.Restrict);

                    entity.HasMany(e => e.SaldosCuenta)
                  .WithOne(e => e.CuentaContable)
               .HasForeignKey(e => e.CuentaContableId)
                         .OnDelete(DeleteBehavior.Restrict);
                });

        // ??????????????????????????????????????????????
        // EjercicioContable
        // ??????????????????????????????????????????????
        modelBuilder.Entity<EjercicioContable>(entity =>
            {
                entity.HasKey(e => e.Id);

                entity.HasMany(e => e.Asientos)
           .WithOne(e => e.Ejercicio)
           .HasForeignKey(e => e.EjercicioId)
             .OnDelete(DeleteBehavior.Restrict);

                entity.HasMany(e => e.SaldosCuenta)
            .WithOne(e => e.Ejercicio)
     .HasForeignKey(e => e.EjercicioId)
          .OnDelete(DeleteBehavior.Restrict);
            });

        // ??????????????????????????????????????????????
        // AsientoContable
        // ??????????????????????????????????????????????
        modelBuilder.Entity<AsientoContable>(entity =>
          {
              entity.HasKey(e => e.Id);

              // Índice compuesto (ClienteId, EjercicioId, Fecha)
              entity.HasIndex(e => new { e.ClienteId, e.EjercicioId, e.Fecha });

              // Autorreferencia: AsientoOrigen ? AsientosReversion (DeleteBehavior.Restrict)
              entity.HasOne(e => e.AsientoOrigen)
           .WithMany(e => e.AsientosReversion)
                    .HasForeignKey(e => e.AsientoOrigenId)
   .OnDelete(DeleteBehavior.Restrict);

              entity.HasMany(e => e.LineasAsiento)
      .WithOne(e => e.Asiento)
            .HasForeignKey(e => e.AsientoId)
           .OnDelete(DeleteBehavior.Restrict);

              entity.HasMany(e => e.Comprobantes)
         .WithOne(e => e.Asiento)
      .HasForeignKey(e => e.AsientoId)
               .OnDelete(DeleteBehavior.Restrict);
          });

        // ??????????????????????????????????????????????
        // LineaAsiento
        // ??????????????????????????????????????????????
        modelBuilder.Entity<LineaAsiento>(entity =>
           {
               entity.HasKey(e => e.Id);

               // Índice compuesto (AsientoId, CuentaContableId)
               entity.HasIndex(e => new { e.AsientoId, e.CuentaContableId });

               // Precisión decimal (18,2) para importes
               entity.Property(e => e.Debe).HasPrecision(18, 2);
               entity.Property(e => e.Haber).HasPrecision(18, 2);
               entity.Property(e => e.ImporteMonedaBase).HasPrecision(18, 2);

               // Precisión decimal (10,4) para tipo de cambio
               entity.Property(e => e.TipoCambio).HasPrecision(10, 4);

               // FK opcional a CentroDeCosto
               entity.HasOne(e => e.CentroCosto)
      .WithMany(e => e.LineasAsiento)
           .HasForeignKey(e => e.CentroCostoId)
              .OnDelete(DeleteBehavior.Restrict);
           });

        // ??????????????????????????????????????????????
        // Comprobante
        // ??????????????????????????????????????????????
        modelBuilder.Entity<Comprobante>(entity =>
        {
            entity.HasKey(e => e.Id);

            // Precisión decimal (18,2) para importes
            entity.Property(e => e.ImporteNeto).HasPrecision(18, 2);
            entity.Property(e => e.ImporteIva).HasPrecision(18, 2);

            // Precisión decimal (5,2) para tasa IVA
            entity.Property(e => e.TasaIva).HasPrecision(5, 2);

            // FK opcional a Importacion
            entity.HasOne(e => e.Importacion)
   .WithMany(e => e.Comprobantes)
         .HasForeignKey(e => e.ImportacionId)
       .OnDelete(DeleteBehavior.Restrict);
        });

        // ??????????????????????????????????????????????
        // Importacion
        // ??????????????????????????????????????????????
        modelBuilder.Entity<Importacion>(entity =>
           {
               entity.HasKey(e => e.Id);
           });

        // ??????????????????????????????????????????????
        // SaldoCuenta
        // ??????????????????????????????????????????????
        modelBuilder.Entity<SaldoCuenta>(entity =>
          {
              entity.HasKey(e => e.Id);

              // Índice compuesto (ClienteId, CuentaContableId, Periodo)
              entity.HasIndex(e => new { e.ClienteId, e.CuentaContableId, e.Periodo });

              // Precisión decimal (18,2) para importes
              entity.Property(e => e.DebeAcumulado).HasPrecision(18, 2);
              entity.Property(e => e.HaberAcumulado).HasPrecision(18, 2);
              entity.Property(e => e.Saldo).HasPrecision(18, 2);
          });

        // ??????????????????????????????????????????????
        // CentroDeCosto
        // ??????????????????????????????????????????????
        modelBuilder.Entity<CentroDeCosto>(entity =>
        {
            entity.HasKey(e => e.Id);

            // Índice único de código dentro del cliente
            entity.HasIndex(e => new { e.ClienteId, e.Codigo }).IsUnique();
        });

        // ??????????????????????????????????????????????
        // TipoDeCambio
        // ??????????????????????????????????????????????
        modelBuilder.Entity<TipoDeCambio>(entity =>
           {
               entity.HasKey(e => e.Id);

               // Índice único (Moneda + Fecha)
               entity.HasIndex(e => new { e.Moneda, e.Fecha }).IsUnique();

               // Precisión decimal (10,4) para valor
               entity.Property(e => e.Valor).HasPrecision(10, 4);
           });

        // ??????????????????????????????????????????????
        // Auditoria
        // ??????????????????????????????????????????????
        modelBuilder.Entity<Auditoria>(entity =>
      {
          entity.HasKey(e => e.Id);
      });

        // ──────────────────────────────────────────────
        // Seed data (roles, permisos, asignaciones)
        // ──────────────────────────────────────────────
        SeedData.Seed(modelBuilder);
    }
}
